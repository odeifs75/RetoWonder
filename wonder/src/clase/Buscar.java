package clase;

public class Buscar {
	private int codRela;
	private String NomUsu;
	//
	public Buscar( ) {
		super();
	
	}
	//
	public int getCodRela() {
		return codRela;
	}
	public void setCodRela(int codRela) {
		this.codRela = codRela;
	}
	public String getNomUsu() {
		return NomUsu;
	}
	public void setNomUsu(String nomUsu) {
		NomUsu = nomUsu;
	}
}
